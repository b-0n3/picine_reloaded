void	ft_putchar(char ch);

int	main(int argc, char **argv)
{
	int x; 
	int y;

	x = 1;
	while ( x < argc)
	{
		y = 0;
		while (argv[x][y] != '\0')
		{
			ft_putchar(argv[x][y]);
			y++; 
		}
		ft_putchar('\n');
		x++;
	}
	return (0);
}
