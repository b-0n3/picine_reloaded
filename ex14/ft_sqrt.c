int	ft_sqrt(int nb)
{
	int i;
	int x;

	x = 1;
	(nb < 12 )? (i = nb): ((nb > 20) ? (i = nb / 4) :( i = nb / 2));
	while (x < i)
	{
		if (x * x == nb)
			return (x);
		x++;
	}
	return (0);
}
